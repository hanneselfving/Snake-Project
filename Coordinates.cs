using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace SnakeProjekt
{
	class Coordinates
    {	
		int x = 0;
		int y = 0;

		public int X
		{
			get
			{
				return x;
			}

			set
			{
				x = value;
			}
		}

		public int Y
		{
			get
			{
				return y;
			}

			set
			{
				y = value;
			}
		}
	}
}
